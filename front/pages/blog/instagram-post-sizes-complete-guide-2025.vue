            <li><strong>Aspect ratio</strong>: 9:16 (vertical full-screen)</li>
            <li><strong>Maximum duration</strong>: 90 seconds</li>
            <li><strong>File size limit</strong>: 4GB</li>
          </ul>
          <p>
            Reels perform best when using the full vertical space, as they're
            designed for immersive viewing.
