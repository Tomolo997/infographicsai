              <strong>Recommended size</strong>: 1080 x 1080 pixels (square),
              1080 x 1350 (portrait), or 1080 x 566 (landscape)
            </li>
            <li><strong>Aspect ratio</strong>: 1:1, 4:5, or 16:9</li>
            <li>
              <strong>Important note</strong>: All images/videos in a carousel
              must use the same aspect ratio
            </li>
          </ul>

